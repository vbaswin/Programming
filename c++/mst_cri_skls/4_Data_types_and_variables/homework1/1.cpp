/*************************************************
enter 2 numbers and do the 4 arithmetic operations
*************************************************/

#include <iostream>
using namespace std;

int main()
{
    cout<< "Enter 2 numbers\n";

    int a,b ;
    cin  >> a >> b;

    // addition
    cout << a << " + " << b << " = " << a + b << "\n" ;
    // subtration
    cout << a << " - " << b << " = " << a - b << "\n" ;
    // division
    cout << a << " / " << b << " = " << a / b << "\n" ;
    // multiplication
    cout << a << " * " << b << " = " << a * b << "\n" ;


    return 0;

}































